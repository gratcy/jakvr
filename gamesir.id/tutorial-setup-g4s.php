			  <h3>Tutorial Setup GameSir G4s</h3>
			  <p>&nbsp;</p>
			  <p>Bagaimana Menghubungkan GameSir G4s pada Android/Tablet/TV/Windows PC serta PS3?</p>
			<p><img src="http://www.gamesir.hk/images/guide_g4s/g4s_connections1.jpg"></p>
			<p>&nbsp;</p>
			<ol>
				<li>Menghubungkan ke Ponsel/Tablet
				<p><img src="http://www.gamesir.hk/images/guide_g4s/g4s_phone1.jpg"></p>
				<p><b>Via Bluetooth</b></p>
				<p>Untuk menghubungkan/menggunakan GameSir G4s ke Ponsel/Tablet Via Bluetooth caranya adalah sebagai berikut :</p>
				<ul>
				<li>Tekan dan tahan secara bersamaan tombol "A"+"Home" selama kira-kira 2 detik hingga indicator mode android berkedip berwarna hijau. Dengan kedipan tersebut menandakan bahwa gamepad sudah siap dikoneksikan ke ponsel/Tablet.</li>
				<li>Langkah selanjutnya adalah pairing gamepad dengan ponsel/tablet, caranya adalah atur pada ponsel/tablet pada setting > nyalakan Bluetooth >cari perangkat baru >tulisan "Gamesir -G4S" akan Nampak pada layar ponsel/tablet dan klik.</li>
				<li>Jika G4S sudah berhasil terhubung dan pairing, makan led indicator mode android akan berhenti berkedip (indicator menyala)</li>
				</ul>
				<p>Jika masih bingung, anda bisa melihat pada video berikut ini :</p>
				<iframe width="560" height="315" src="https://www.youtube.com/embed/1mJWovgFqYg" frameborder="0" allowfullscreen></iframe>
				</li>
				<li>Menghubungkan ke Android TV
				<p><img src="http://www.gamesir.hk/images/guide_g4s/g4s_tvbox.jpg"></p>
				<p><b>Via Dongler (Wireless  2,4 Ghz)</b></p>
				<ul>
				<li>Keluarkan Dongler yang terletak di tengah-tengah gamepad ( dongler berada dibawah indicator mode)</li>
				<li>Colokkan dongler pada port USB TV BOX, setelah itu tekan dan tahan tombol "A" + "Home" secara bersamaan selama 2detik (hingga TV mode indicator berhenti berkedip dan menyala)</li>
				<li>Apabila indicator berhenti berkedip dan menyala berarti G4s sudah berhasil terkoneksi dengan TV BOX</li>
				</ul>
				<p>Jika masih bingung, anda bisa melihat pada video berikut ini :</p>
				<iframe width="560" height="315" src="https://www.youtube.com/embed/vHv-PDgqeB8" frameborder="0" allowfullscreen></iframe>
				<p>&nbsp;</p>
				<p><b>Via Kabel USB (Wired)</b></p>
				<ul>
				<li>Tekan dan tahan tombol "A" + "Home" secara bersamaan selama 2 detik (indicator mode akan berkedip )</li>
				<li>Ambil Kabel USB yang ada pada kardus G4s. Colokkan Kabel USB (kepala yang kecil) pada G4S (tempat kabel charger) dan colokkan ujung kabel USB lainnya (yang besar) pada Port USB Android TV BOX.</li>
				<li>Apabila berhasil terkoneksi, led mode indicator akan menyala (berhenti berkedip)</li>
				</ul>
				<p>Jika masih bingung, anda bisa melihat pada video berikut ini :</p>
				<iframe width="560" height="315" src="https://www.youtube.com/embed/Rq5YQ8I81EA" frameborder="0" allowfullscreen></iframe>
				<p>&nbsp;</p>
				<p><b>Via Bluetooth</b></p>
				<ul>
					<li>Tekan dan tahan tombol "A" + "Home" secara bersamaan selama 2 detik (indicator mode akan berkedip )</li>
					<li>Apabila indicator mode android tetap berkedip, itu artinya gamepad telah siap untuk dihubungkan ke android TV BOX</li>
					<li>Langkah selanjutnya adalah pairing gamepad, untuk itu buka Bluetooth android TV dan cari  "GameSir-G4s" yang muncul pada layar android TV BOX. Klik untuk menghubungkan Via Bluetooth.</li>
					<li>Jika berhasil terhubung dan pairing, maka indicator mode android akan berhenti berkedip dan menyala.</li>
				</ul>
				<p>Jika masih bingung, anda bisa melihat pada video berikut ini :</p>
				<iframe width="560" height="315" src="https://www.youtube.com/embed/KDjjn2rN--E" frameborder="0" allowfullscreen></iframe>
				</li>
				<li>Menghubungkan ke Windows PC
				<p><img src="http://www.gamesir.hk/images/guide_g4s/g4s_pc.jpg"></p>
				<p>&nbsp;</p>
				<p><b>Via Dongler (Wireless  2,4 Ghz)</b></p>
				<ul>
					<li>Keluarkan Dongler yang terletak di tengah-tengah gamepad ( dongler berada dibawah indicator mode)</li>
					<li>Colokkan dongler pada port USB TV BOX, setelah itu tekan dan tahan tombol "X" + "Home" secara bersamaan selama 2 detik (hingga PC mode indicator berhenti berkedip dan menyala)</li>
					<li>Apabila indicator berhenti berkedip dan menyala berarti G4s sudah berhasil terkoneksi dengan PC</li>
				</ul>
				<p>Jika masih bingung, anda bisa melihat pada video berikut ini :</p>
				<iframe width="560" height="315" src="https://www.youtube.com/embed/Z6MnNBZHUE4" frameborder="0" allowfullscreen></iframe>
				<p><b>Via Kabel USB (Wired)</b></p>
				<ul>
				<li>Tekan dan tahan tombol "X" + "Home" secara bersamaan selama 2 detik (indicator mode akan berkedip )</li>
				<li>Ambil Kabel USB yang ada pada kardus T1s. Colokkan Kabel USB (kepala yang kecil) pada G4s (tempat kabel charger) dan colokkan ujung kabel USB lainnya (yang besar) pada Port USB  PC.</li>
				<li>Apabila berhasil terkoneksi, led mode indicator akan menyala (berhenti berkedip)</li>
				</ul>
				<p>Jika masih bingung, anda bisa melihat pada video berikut ini :</p>
				<iframe width="560" height="315" src="https://www.youtube.com/embed/jH19nQgQyo8" frameborder="0" allowfullscreen></iframe>
				</li>
				<li>Menghubungkan ke PS 3
				<p><img src="http://www.gamesir.hk/images/guide_g4s/g4s_ps3.jpg"></p>
				<p>&nbsp;</p>
				<p><b>Via Dongler (Wireless  2,4 Ghz)</b></p>
				<ul>
				<li>Keluarkan Dongler yang terletak di tengah-tengah gamepad ( dongler berada dibawah indicator mode)</li>
				<li>Colokkan dongler pada port USB PS 3, setelah itu tekan dan tahan tombol "Turbo" + "Home" secara bersamaan selama 2 detik (hingga PS 3 mode indicator berhenti berkedip dan menyala)</li>
				<li>Apabila indicator berhenti berkedip dan menyala berarti G4s sudah berhasil terkoneksi dengan PS 3</li>
				</ul>
				<p>Jika masih bingung, anda bisa melihat pada video berikut ini :</p>
				<iframe width="560" height="315" src="https://www.youtube.com/embed/gvHw1u3jRCo" frameborder="0" allowfullscreen></iframe>
				<p>&nbsp;</p>
				<p><b>Via Kabel USB (Wired)</b></p>
				<ul>
				<li>Tekan dan tahan tombol "Turbo" + "Home" secara bersamaan selama 2 detik (indicator mode akan berkedip )</li>
				<li>Ambil Kabel USB yang ada pada kardus T1s. Colokkan Kabel USB (kepala yang kecil) pada G4s (tempat kabel charger) dan colokkan ujung kabel USB lainnya (yang besar) pada Port USB  PS 3.</li>
				<li>Apabila berhasil terkoneksi, led mode indicator akan menyala (berhenti berkedip)</li>
				</ul>
				<p>Jika masih bingung, anda bisa melihat pada video berikut ini :</p>
				<iframe width="560" height="315" src="https://www.youtube.com/embed/mWO7e5SZlU0" frameborder="0" allowfullscreen></iframe>
				</li>
			</ol>
